using System;

class Root
{
    static void Main(string[] args)
    {
        // while loop
        int i = 0;
        while (i < 5)
        {
            Console.WriteLine(i);
            i++;
        }
        // for loop
        for (int j = 0; j < 5; j++)
        {
            Console.WriteLine(j);
        }
        // do while loop
        int k = 0;
        do
        {
            Console.WriteLine(k);
            k++;
        } while (k < 5);


    }

}